"""
UIFixEngine — Self-Healing UI Generator
========================================

When RuntimeValidator.check_missing_ui() returns issues, this engine:
  1. Maps each issue to concrete React component/page files
  2. Generates code via LLM (or falls back to high-quality static templates)
  3. Safely writes files (backing up any existing content)
  4. Patches App.tsx to integrate new routes + imports

Safety guarantees:
  - Existing files are backed up before modification
  - New files are created only (never overwrite working components)
  - All methods are try/except wrapped — failures are logged, not raised
"""

